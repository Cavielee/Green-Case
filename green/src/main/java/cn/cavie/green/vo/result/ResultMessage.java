package cn.cavie.green.vo.result;

public class ResultMessage {

	// 跳转地址
	private String url;

	// 信息反馈
	private String msg;

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}
}
